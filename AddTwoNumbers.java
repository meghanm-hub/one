public class AddTwoNumbers {
    public static void main(String[] args) {
        // Static input
        int number1 = 10; // First number
        int number2 = 20; // Second number

        // Adding two numbers
        int sum = number1 + number2;

        // Output the result
        System.out.println("The sum of " + number1 + " and " + number2 + " is: " + sum);
    }
}
